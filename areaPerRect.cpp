// This program calculates and the area and
// perimeter of a rectangle with dimensions 10cm and 17cm
#include <iostream>

int main() {
    std::cout << "For a rectangle that has dimensions\n";
    std::cout << "length 10cm and width 17cm:\n";
    std::cout << "\n";
    std::cout << "Area = " << 10 * 17 << " cm^2\n";
    std::cout << "Perimeter = " << 2 * (10 + 17) << " cm\n";
}
